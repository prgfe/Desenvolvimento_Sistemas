namespace Calc_POO
{
    public class Calculadora
    {
        public float Somar(float num1, float num2)
        {
            return num1 + num2;
        }
        public float Subtrair(float num1, float num2)
        {
            return num1 - num2;
        }
        public float Multiplicar(float num1, float num2)
        {
            return num1 * num2;
        }
        public float Dividir(float num1, float num2)
        {
            return num1 / num2;
        }
    }
}